<h1>Integrating with OpenACH</h1>

<p>One of the great benefits of using OpenACH is how easily it can be integrated with websites, e-commerce systems, invoicing systems, etc. The API can be used to create and maintain payment profiles, bank accounts, and payment schedules. Payment profiles can easily be linked to your customer records using an 'external id', making it very easy to find your customers later if you don't want to have to track the IDs that OpenACH creates for them.</p>
<p>To find out more about how you can integrate your application with OpenACH, take a look at the <a href="http://openach.com/books/openach-developer-guide">OpenACH Developer Guide</a> on the <a href="http://openach.com/">OpenACH.com</a> website.</p>
